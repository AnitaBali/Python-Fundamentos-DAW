#Imprimir desde indice:hasta indice
texto="ABCDEFGHIJKLM"
fragmento=texto[2:5]
print(fragmento)

#Imprimir desde indice indicado :hasta el final
texto="ABCDEFGHIJKLM"
fragmento=texto[2:]
print(fragmento)

#Imprimir desde inicio hasta indice indicado
texto="ABCDEFGHIJKLM"
fragmento=texto[:5]
print(fragmento)

#Imprimir desde:hasta saltando según numero indicado
texto="ABCDEFGHIJKLM"
fragmento=texto[2:10:2]
print(fragmento)

#Imprimir desde:hasta saltando según numero indicado
texto="ABCDEFGHIJKLM"
fragmento=texto[2:10:2]
print(fragmento)

#Imprimir desde el revés
texto="ABCDEFGHIJKLM"
fragmento=texto[::-1]
print(fragmento)

