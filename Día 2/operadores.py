x=6
y=2
z=7

print(f"{x} mas {y} es igual a {x+y}")
print(f"{x} menos {y} es igual a {x-y}")
print(f"{x} multiplicado por {y} es igual a {x*y}")
print(f"{x} dividido entre {y} es igual a {x/y}")

#División al piso, cuando divides y redondea al mínimo, quita decimales, usamos doble barra
print(f"{x} dividido al piso de {y} es igual a {x//y}")

#Módulo, que dará el resto de una división, usamos el porcentaje
print(f"{x} módulo de {y} es igual a {x%y}")

#Potencia al cuadrado, usaremos dos veces el asterisco
print(f"{x} elevado a {y} es igual a {x**y}")

#Potencia a un número x
print(f"{x} elevado a {3} es igual a {x**3}")

#Raíz cuadrada de un número, se pone la variable multiplicada dos veces por 0.5
print(f"La raíz cuadrada de {x} es {x**0.5}")